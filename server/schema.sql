CREATE TABLE CLASS (
    CLASS_ID VARCHAR(13),
    COLOR VARCHAR(18),
    PRIMARY KEY (CLASS_ID)
);

CREATE TABLE TASK (
    TASK_ID INT AUTO_INCREMENT,
    DATE TIMESTAMP,
    TITLE TEXT,
    CLASS_ID VARCHAR(12),
    NOTES TEXT,
    PRIORITY INT,
    PRIMARY KEY (TASK_ID),
    FOREIGN KEY(CLASS_ID) REFERENCES CLASS(CLASS_ID) ON DELETE SET NULL
);

